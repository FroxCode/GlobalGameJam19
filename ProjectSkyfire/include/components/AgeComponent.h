#ifndef _AGECOMPONENT_H_
#define _AGECOMPONENT_H_

#include "Component.h"

class AgeComponent : public Component
{
public:

	AgeComponent() : Component("AgeComponent") {}
	////////////////////////////////////////////////////////////
	/// Overloaded constructor
	////////////////////////////////////////////////////////////
	AgeComponent(int age = 18) : Component("AgeComponent"), m_age(age) {}
	////////////////////////////////////////////////////////////
	/// Destructor (for overriding virtual function in abstract 
	/// base Component)
	////////////////////////////////////////////////////////////
	~AgeComponent() {}

	////////////////////////////////////////////////////////////
	/// Getters/Setters
	////////////////////////////////////////////////////////////
	int getAge() { return m_age; }
	void setBox(int age) { m_age = age; }


private:
	////////////////////////////////////////////////////////////
	/// Member Variables
	////////////////////////////////////////////////////////////
	int m_age;
};
#endif;